"""Write a program that asks the user for a
weight in kilograms and converts it to pounds.
There are 2.2 pounds in a kilogram."""




kilograms = float(input("Enter weight in kilograms: "))



pounds = kilograms * 2.2



print("Display weight in Pounds",round(pounds,2))












# Step 1: Ask the user to enter weight in kilograms

# Step 2: Convert kilograms to pounds using the formula
# 1 kilogram = 2.2 pounds
# Step 3: Display the result
